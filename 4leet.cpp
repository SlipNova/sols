int findMedianSortedArrays(vector <int>& a, vector <int>& b) {
    int m = a.size();
    int n = b.size();
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tt;
    cin >> tt;
    while (tt--) {
        int m, n;
        cin >> m >> n;
        vector <int> a(m), b(n);
        for (int i = 0; i < n; i++) {
            cin >> a[i];
        }
        for (int i = 0; i < m; i++) {
            cin >> b[i];
        }
        cout << findMedianSortedArrays(a, b) << '\n';
    }
    return 0;
}
